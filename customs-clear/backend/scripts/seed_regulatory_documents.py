def _make_url(agency: str, num: str) -> str:
    """Generate a deterministic URL for a document."""
    agency_urls = {
        "FTS": "https://customs.gov.ru/document/",
        "EEC": "https://docs.eaeunion.org/docs/",
        "MPT": "https://minpromtorg.gov.ru/docs/",
        "RPN": "https://rospotrebnadzor.ru/documents/",
        "RSN": "https://fsvps.gov.ru/docs/",
    }
    base = agency_urls.get(agency, "https://docs.gov.ru/")
    return f"{base}{num.replace('/', '-').replace(' ', '_')}"


