def _generate_all_docs() -> list[dict]:
    """Generate the full set of documents."""
    all_docs = []

    for ch, info in CHAPTERS.items():
        agencies = info.get("agencies", [])

        if "FTS" in agencies or not agencies:
            all_docs.extend(_gen_fts_docs(ch, info))
        if "EEC" in agencies:
            all_docs.extend(_gen_eec_docs(ch, info))
        if "MPT" in agencies:
            all_docs.extend(_gen_mpt_docs(ch, info))
        if "RPN" in agencies:
            all_docs.extend(_gen_rpn_docs(ch, info))
        if "RSN" in agencies:
            all_docs.extend(_gen_rsn_docs(ch, info))

        all_docs.extend(_gen_pravo_docs(ch, info))

    all_docs.extend(_gen_special_docs())

    return all_docs


def run():
    all_docs = _generate_all_docs()
    print(f"Generated {len(all_docs)} documents to seed")

    session = SessionLocal()
    try:
        docs_added = 0
        docs_skipped = 0
        mappings_added = 0

        for d in all_docs:
            url = _url(d["agency"], d["num"])
            doc_id = _doc_id(url)

            existing = session.execute(
                text("SELECT id FROM regulatory_documents WHERE id = :id"),
                {"id": doc_id},
            ).fetchone()

            if existing:
                docs_skipped += 1
                continue

            if DRY_RUN:
                docs_added += 1
                mappings_added += len(d.get("hs", []))
                continue

            doc_date_val = None
            if d.get("date"):
                try:
                    doc_date_val = datetime.strptime(d["date"], "%Y-%m-%d").date()
                except (ValueError, TypeError):
                    pass

            doc = RegulatoryDocument(
                id=doc_id,
                agency=d["agency"],
                doc_type=d["doc_type"],
                doc_number=d.get("num"),
                doc_date=doc_date_val,
                title=d["title"],
                body=d.get("body", d["title"]),
                source_url=url,
                status="active",
                quality="verified",
            )
            session.add(doc)

            for hs_prefix in d.get("hs", []):
                mapping = RegulatoryDocHsMapping(
                    doc_id=doc_id,
                    hs_prefix=str(hs_prefix),
                    scope="import",
                    relevance=d.get("relevance", "direct"),
                    confidence=d.get("confidence", 1.0),
                    source="seed",
                    note=f"Mass seed: {d['title'][:100]}",
                )
                session.add(mapping)
                mappings_added += 1

            try:
                session.flush()
                docs_added += 1
            except IntegrityError:
                session.rollback()
                docs_skipped += 1

        if not DRY_RUN:
            session.commit()

        total_docs = session.execute(text("SELECT COUNT(*) FROM regulatory_documents")).scalar()
        total_maps = session.execute(text("SELECT COUNT(*) FROM regulatory_doc_hs_mapping")).scalar()
        by_agency = session.execute(text(
            "SELECT agency, COUNT(*) FROM regulatory_documents GROUP BY agency ORDER BY COUNT(*) DESC"
        )).fetchall()

        print(f"\n{'=' * 60}")
        print(f"{'DRY-RUN ' if DRY_RUN else ''}Results:")
        print(f"  Documents added:   {docs_added}")
        print(f"  Documents skipped: {docs_skipped}")
        print(f"  HS mappings added: {mappings_added}")
        print(f"  Total documents:   {total_docs}")
        print(f"  Total HS mappings: {total_maps}")
        print(f"\n  By agency:")
        for r in by_agency:
            print(f"    {r[0]:20s}: {r[1]}")
        print(f"{'=' * 60}")

    finally:
        session.close()


if __name__ == "__main__":
    run()
