def seed() -> dict[str, int]:
    from app.models.tnved import ClassificationRuling
    Base.metadata.create_all(engine, tables=[ClassificationRuling.__table__])

    inserted = 0
    with SessionLocal() as db:
        for r in RULINGS:
            exists = db.execute(
                text("SELECT 1 FROM classification_rulings WHERE ruling_number = :rn"),
                {"rn": r["num"]},
            ).fetchone()
            if exists:
                continue
            row = ClassificationRuling(
                ruling_number=r["num"],
                ruling_date=r["date"],
                agency=r["agency"],
                goods_description=r["desc"],
                assigned_hs_code=r["hs"],
                rationale=r["rationale"],
                source_url=r.get("url", ""),
            )
            db.add(row)
            inserted += 1

        if DRY_RUN:
            db.rollback()
            print(f"[DRY RUN] Would insert {inserted} classification rulings")
        else:
            db.commit()
            print(f"Inserted {inserted} classification rulings")

        total = db.execute(text("SELECT COUNT(*) FROM classification_rulings")).scalar()
        by_agency = db.execute(text(
            "SELECT agency, COUNT(*) FROM classification_rulings GROUP BY agency ORDER BY agency"
        )).fetchall()
        print(f"Total: {total}")
        for a, c in by_agency:
            print(f"  {a}: {c}")

    return {"inserted": inserted, "total": total}


if __name__ == "__main__":
    seed()
