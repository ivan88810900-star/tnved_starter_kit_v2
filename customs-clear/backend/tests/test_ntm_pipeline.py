@pytest.mark.parametrize("hs_code,description,expected", REGRESSION_MATRIX)
def test_ntm_detailed_regulations(hs_code: str, description: str, expected: set[tuple[str, str | None]]) -> None:
    result = asyncio.run(
        check_position_non_tariff(
            hs_code=hs_code,
            description=description,
            country="CN",
            permits=[],
            skip_registry_verify=True,
        )
    )
    permits = result.get("required_permits") or []
    got = {(str(p["permit_type"]), p.get("tr_ts")) for p in permits}

    assert got == expected, (
        f"Regulation mismatch for {hs_code} '{description}':\n"
        f"  Expected: {sorted(expected)}\n"
        f"  Got:      {sorted(got)}\n"
        f"  Missing:  {sorted(expected - got)}\n"
        f"  Extra:    {sorted(got - expected)}\n"
    )
