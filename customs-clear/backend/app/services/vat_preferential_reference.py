def match_preferential_vat_group(hs_code: str) -> dict[str, Any] | None:
    """
    Возвращает {act, category, prefix} для самого длинного префикса из справочника,
    совпадающего с 10-значным кодом, иначе None.
    """
    digits = re.sub(r"\D", "", hs_code or "")[:10]
    if len(digits) < 4:
        return None
    best: dict[str, Any] | None = None
    best_len = 0
    for prefix, meta in PREFERENTIAL_VAT_GROUP_BY_PREFIX.items():
        if digits.startswith(prefix) and len(prefix) > best_len:
            best = {**meta, "prefix": prefix}
            best_len = len(prefix)
    return best
