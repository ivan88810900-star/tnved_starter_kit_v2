def _sorted_unique_prefixes(prefixes: list[str]) -> list[str]:
    """Длинные префиксы раньше, чтобы 85256 раньше 8525 при необходимости."""
    uniq = sorted({p for p in prefixes if p}, key=len, reverse=True)
    return uniq


def _matches_prefix(hs_code: str, prefixes: list[str]) -> str | None:
    """Возвращает первый совпавший префикс или None."""
    code = (hs_code or "").strip()
    for prefix in _sorted_unique_prefixes(prefixes):
        if code.startswith(prefix):
            return prefix
    return None


def get_vet_requirement(hs_code: str) -> dict | None:
    """Ветеринарный сертификат по Решению ЕЭК №317."""
    code = (hs_code or "").strip()
    matched = _matches_prefix(code, VET_DOMAINS)
    if not matched:
        return None
    return {
        "permit_type": "ВС",
        "tr_ts": None,
        "tr_ts_full_name": "Ветеринарный контроль",
        "description": "Ветеринарный сертификат страны экспорта + ВС ЕЭК",
        "legal_ref": "Решение КТС №317 от 18.06.2010 (Единый перечень подконтрольных товаров)",
        "matched_prefix": matched,
        "priority": 1,
        "trigger": None,
    }


