def get_sgr_requirement(hs_code: str, description: str = "") -> dict | None:
    """СГР по Решению ЕЭК №299 (по HS-домену или триггеру в описании)."""
    code = (hs_code or "").strip()
    desc_lower = (description or "").lower()

    matched_prefix = _matches_prefix(code, SGR_DOMAINS)
    matched_trigger: str | None = None

    for trigger in SGR_DESCRIPTION_TRIGGERS:
        if trigger in desc_lower:
            matched_trigger = trigger
            break

    # 2201: обычная питьевая вода без признаков регистрации — не цепляем СГР по одному HS
    if matched_prefix == "2201" and not matched_trigger:
        water_sgr_hints = (
            "минеральн",
            "лечеб",
            "столов",
            "бутилир",
            "детск",
            "газирован",
        )
        if not any(h in desc_lower for h in water_sgr_hints):
            matched_prefix = None

    if not matched_prefix and not matched_trigger:
        return None

    return {
        "permit_type": "СГР",
        "tr_ts": None,
        "tr_ts_full_name": "Государственная регистрация Роспотребнадзора",
        "description": "Свидетельство о государственной регистрации (СГР)",
        "legal_ref": "Решение ЕЭК №299 (Перечень I раздел II)",
        "matched_prefix": matched_prefix or (code[:4] if len(code) >= 4 else code),
        "priority": 1,
        "trigger": matched_trigger,
    }


