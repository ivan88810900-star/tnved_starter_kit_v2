def get_all_layer_requirements(hs_code: str, description: str = "") -> list[dict]:
    """Возвращает ВСЕ применимые нерегламентные требования."""
    result: list[dict] = []
    for fn in (
        get_vet_requirement,
        get_phyto_requirement,
        get_nf_requirement,
        get_licence_requirement,
    ):
        req = fn(hs_code)
        if req:
            result.append(req)
    sgr = get_sgr_requirement(hs_code, description)
    if sgr:
        result.append(sgr)
    return result
