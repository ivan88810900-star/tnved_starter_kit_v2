from __future__ import annotations


def calculate_customs_fee(customs_value_rub: float) -> float:
    """
    Таможенные сборы РФ (2026), руб.

    Шкала:
      - до 200 000 включительно: 1 231
      - 200 000,01 .. 450 000: 2 462
      - 450 000,01 .. 1 200 000: 4 924
      - 1 200 000,01 .. 2 700 000: 13 541
      - 2 700 000,01 .. 4 200 000: 18 465
      - 4 200 000,01 .. 5 500 000: 21 344
      - 5 500 000,01 .. 10 000 000: 49 240
      - свыше 10 000 000: 73 860
    """
    v = float(customs_value_rub or 0.0)
    if v <= 0:
        return 0.0
    if v <= 200_000:
        return 1_231.0
    if v <= 450_000:
        return 2_462.0
    if v <= 1_200_000:
        return 4_924.0
    if v <= 2_700_000:
        return 13_541.0
    if v <= 4_200_000:
        return 18_465.0
    if v <= 5_500_000:
        return 21_344.0
    if v <= 10_000_000:
        return 49_240.0
    return 73_860.0

