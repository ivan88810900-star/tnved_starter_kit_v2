"""Каталог регламентов ТР ТС/ТР ЕАЭС.

Единый источник привязки HS → регламент → форма (СС/ДС) для брокерского слоя
и для ``get_default_cert_form`` (см. ``non_tariff_rules``).

Один HS-код может попадать в несколько регламентов одновременно.

Нерегламентные слои ЕАЭС (ветеринария, фитосанитария, СГР, нотификация ФСБ,
лицензирование импорта) задаются в ``ntm_layers`` и подмешиваются в ответ
брокера через ``get_full_ntm_requirements`` (ТР ТС + слои в одном порядке).

Слой ТР ТС в ``get_full_ntm_requirements`` при ``NTM_V2_TR_TS_ENABLED`` берётся
из БД (``ntm_engine_v2``), иначе — из ``get_tr_ts_requirements`` (этот модуль).

Нерегламентные слои (``ntm_layers``) при ``NTM_V2_LAYERS_ENABLED`` также могут
читаться из БД через ``ntm_engine_v2``; иначе — ``get_all_layer_requirements``.
"""
