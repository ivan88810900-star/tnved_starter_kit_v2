def get_tr_ts_requirements(hs_code: str) -> list[dict[str, Any]]:
    """Возвращает применимые регламенты ТР ТС/ЕАЭС для HS-кода."""
    code = normalize_hs_code(hs_code)
    if not code:
        return []

    result: list[dict[str, Any]] = []
    seen: set[tuple[str, str]] = set()

    for prefix, tr_ts, form in ALL_REGULATIONS:
        if match_hs_prefix(code, prefix):
            key = (tr_ts, form)
            if key in seen:
                continue
            seen.add(key)
            result.append(
                {
                    "permit_type": form,
                    "tr_ts": tr_ts,
                    "tr_ts_full_name": TR_TS_FULL_NAMES.get(tr_ts, ""),
                    "description": (
                        f"{'Декларация о соответствии' if form == 'ДС' else 'Сертификат соответствия'} "
                        f"по ТР ТС {tr_ts}"
                    ),
                    "legal_ref": f"ТР ТС {tr_ts}",
                    "matched_prefix": prefix,
                    "priority": 1,
                    "trigger": None,
                }
            )

    return result


