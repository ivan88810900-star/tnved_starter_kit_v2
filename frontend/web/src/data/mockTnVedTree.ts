export function normalizeHsDigits(code: string): string {
  return (code || "").replace(/\D/g, "");
}

export function getLocalFullTitle(code: string): string | null {
  const d = normalizeHsDigits(code);
  if (!d) return null;
  return LOCAL_FULL_TITLES[d] ?? null;
}

/**
 * Итоговое наименование для отображения.
 * Порядок приоритета:
 *   1) title_full из бэкенда (ETL — наш источник истины);
 *   2) локальный оверлей для отдельных выбранных кодов;
 *   3) очищенное короткое title_ru из API;
 *   4) сам код (чтобы никогда не получить пустую строку).
 */
export function resolvedNomenclatureTitle(
  code: string,
  apiTitle: string | null | undefined,
  apiTitleFull?: string | null,
): string {
  const full = (apiTitleFull || "").trim();
  if (full) return full;
  return (getLocalFullTitle(code) ?? cleanTitle(apiTitle)) || code;
}

export type WithCodeAndTitle = {
  code: string;
  title_ru: string | null;
  title_full?: string | null;
};

/** Подмешивает полный текст из локального каталога, если он есть для данного кода
 * и бэкенд не вернул более длинный title_full. */
export function mergeNodesWithLocalTitles<T extends WithCodeAndTitle>(nodes: T[]): T[] {
  return nodes.map((n) => {
    const local = getLocalFullTitle(n.code);
    if (!local) return n;
    const current = (n.title_full || n.title_ru || "").trim();
    // Берём локальный оверлей только если он заметно длиннее/точнее бэкенда
    if (current.length >= local.length) return n;
    return { ...n, title_full: n.title_full || local, title_ru: n.title_ru ?? local };
  });
}
